// Factorial

//all positive descending integers. Factorial of n is denoted by n!.

package BasicsCode;

public class Factorialmain {

	public static void main(String[] args) {
		int num =18;
		long factorial=1;
		for (int i=1;i<=num;++i) {
			factorial *=1;
		}
		System.out.println("Factorial of"+num+"="+factorial);
		
		
	}

}
